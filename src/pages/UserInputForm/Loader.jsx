import React, { useState, useEffect } from "react";

const Loader = () => {
  const [currentLoader, setCurrentLoader] = useState(0);

  useEffect(() => {
    const interval = setInterval(() => {
      setCurrentLoader((prevLoader) => (prevLoader + 1) % loaders.length);
    }, 500); // Change every 0.5 seconds

    return () => clearInterval(interval); // Cleanup on unmount
  }, []);

  // Get the current loader component to render
  const CurrentLoaderComponent = loaders[currentLoader];

  return (
    <div>
      <CurrentLoaderComponent />
    </div>
  );
};

